<?php
$n = 12.324;
$o = 017;
$h = 0X1B;
$b = 0101;

printf("Number in Decimal: %d\n",$n);       //Decimal print
printf("Number in Octal: %o\n",$o);         //Octal print
printf("Number in Hexadecimal: %X\n",$h);   //Hexadecimal print
printf("Number in Binary: %b\n",$b);        //Binary print

printf("Number in Decimal: %05d\n",$n);     //figure fixed
printf("Number in Decimal: %.2d\n",$n);     //fixed value after point
